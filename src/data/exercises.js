// Get all muscle groups
export const getMuscleGroups = () => {
  return Object.keys(exerciseDatabase)
}

// Get exercises for a specific muscle group
export const getExercisesForMuscleGroup = (muscleGroup) => {
  return exerciseDatabase[muscleGroup] || []
}

// Search exercises across all muscle groups
export const searchExercises = (query) => {
  const results = []
  const lowerQuery = query.toLowerCase()
  
  Object.entries(exerciseDatabase).forEach(([muscleGroup, exercises]) => {
    exercises.forEach(exercise => {
      if (exercise.toLowerCase().includes(lowerQuery)) {
        results.push({
          exercise,
          muscleGroup,
          category: muscleGroup.charAt(0).toUpperCase() + muscleGroup.slice(1)
        })
      }
    })
  })
  
  return results
}

// Get total number of exercises
export const getTotalExerciseCount = () => {
  return Object.values(exerciseDatabase).reduce((total, exercises) => total + exercises.length, 0)
}
